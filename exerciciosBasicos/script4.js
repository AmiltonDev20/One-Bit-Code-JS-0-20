
let x = parseInt(prompt("Digite o primeiro número: "))
let y = parseInt(prompt("Digite o segundo número: "))

resSoma = x + y;
resSub = x - y;
resMul = x * y;
resDiv = x / y;

alert(`O resultado das Operações são:\n 
    Soma: ${x} + ${y} = ${resSoma}\n
    Subtração: ${x} - ${y} = ${resSub}\n
    Multiplicação: ${x} * ${y} = ${resMul}\n
    Divisão: ${x} / ${y} = ${resDiv}`)