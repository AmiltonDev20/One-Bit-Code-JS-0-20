
const resultado = parseFloat(prompt("Digite um numero de 1 a 5: "));

switch (resultado){
    case 1:
        alert("Você escolheu a opção 1!")
        break
    case 2:
        alert("Você escolheu a opção 2!")
        break
    case 3:
        alert("Você escolheu a opção 3!")
        break
    case 4:
        alert("Você escolheu a opção 4!")
        break
    case 5:
        alert("Você escolheu a opção 5!")
        break
    default:
        alert("Nenhuma opção disponivel foi escolhda")    
}